const ROOT = {
    "text": "Root", 
    "children": [ BRNADS, RANDOM ]
}

class DemoData {
    buildNode() {
        return {
            text: 'Item', 
            children: [],  
            isExpanded: true,
            $level: 0
        }
    }

    buildTree(parent, pattern = [3,3,3]) {
        if(!pattern.length) { return; }
        parent = parent || this.buildNode();

        for(let i = 1; i <= pattern[0]; i++) {
            let node = this.buildNode();
            node.text = parent.text + '.' + i,
            parent.children.push(node);
        }

        parent.children.map(child => this.buildTree(child, pattern.slice(1)));

        if(parent.text == 'Item') {
            parent.text = 'root';
        }

        return parent;
    }

    getBrandsData() {
        return BRNADS;
    }

    getRoot() {
        let root = { ...ROOT, children: [ ...ROOT.children ] };
        
        root.children[0] = { ...BRNADS, children: [], childrenCount: BRNADS.children.length };

        return root;
    }
    
    getNodeById(tree, id) {
        if(tree.id === id) { return tree; }

        let result = null;

        if(tree.children) {
            tree.children.map(child => {
                let node = this.getNodeById(child, id);
                if(node) { result = node };
            });
        }

        return result;
    }

    getChildrenById(id) {
        if(id === "00000000-0000-0000-0000-000000000000") {
            return this.getRandomChildren();
        } 

        let children = this.getNodeById(ROOT, id).children || [];
        let nodes = [];

        children.forEach((node, i) => {
            nodes[i] = Object.assign({}, node);
            if(node.isTerminal) {
                nodes[i] = { ...node };
            } else {
                nodes[i] = { ...node, children: [], childrenCount: node.children.length };
            }
        });

        return nodes;
    }

    getRandomChildren() {
        const rand1 = Math.round(Math.random() * 100);
        const nodes = new Set();

        for(let i = rand1; i > 0; i--) {
            const rand2 = Math.round(Math.random() * 100);
            const rand3 = Math.round(Math.random() * 100);
            const randomFolder = BRNADS.children[rand1 % BRNADS.children.length];
            const randomNode = randomFolder.children[rand2 % randomFolder.children.length];
            nodes.add(randomNode);
        }

        return Array.from(nodes.entries(), entry => ({
            ...entry[0], 
            children: [], 
            childrenCount: 1, 
            isTerminal: (Math.random() > 0.5),
            id: "00000000-0000-0000-0000-000000000000"
        }));
    }

    newGuid() {
        var guid = "";
        for (var i = 1; i <= 32; i++){
          var n = Math.floor(Math.random()*16.0).toString(16);
          guid +=   n;
          if((i==8)||(i==12)||(i==16)||(i==20))
            guid += "-";
        }
        return guid;
    }
}

